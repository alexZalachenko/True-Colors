void pintarEscenario(u8 sala){
  u8 a,b;
  u8 const * tile=0;//u8 const * tile=NULL;
  u8* membal;
  cpct_clearScreen(cpct_px2byteM1(2,2,2,2));
  for(a=0;a<12;a++){
    for(b=0;b<20;b++){
        switch(mapas[sala].posiciones[a][b]){
          case 0:
            continue;
          break;

          case 1:
            tile = pared;
          break;

          case 2:
            tile = puerta1;
          break;

          case 3:
            tile = puerta2;
          break;

          case 4:
            tile = puertaDer1;
          break;

          case 5:
            tile = puertaDer2;
          break;

          case 6:
            tile = puertaIz1;
          break;

          case 7:
            tile = puertaIz2;
          break;

          case 8:
            tile = columna;
          break;
        }
        //PINTO EL TILE CORRESPONDIENTE
        membal= cpct_getScreenPtr(video_memory_start, (4*b), (16*a));
        cpct_drawSprite(tile, membal, 4, 16);
    }
  }
}

